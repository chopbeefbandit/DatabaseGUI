    private void rental_SearchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rental_SearchbtnActionPerformed
        // TODO add your handling code here:
        ArrayList<Vehicle> vehicleList;
        ArrayList<Customer> customerList;
        DefaultTableModel model = (DefaultTableModel) available_table.getModel();
        DefaultTableModel model1 = (DefaultTableModel) rental_accountTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[5];
        Object[] row1 = new Object[3];
        
        try {
            String type = rental_typeInputField.getText();
            String category = rental_categLabel.getText();
            String startDate = rental_date1Label.getText();
            String returnDate = rental_date2Label.getText();
            
            vehicleList = connector.searchRentals(type, category, startDate, returnDate);
            for (int i = 0; i<vehicleList.size(); i++)
            {
                row[0] = vehicleList.get(i).getVin();
                row[1] = vehicleList.get(i).getDescription();
                row[2] = vehicleList.get(i).getYear();
                row[3] = vehicleList.get(i).getType();
                row[4] = vehicleList.get(i).getCategory();
                model.addRow(row);
                    
            }
            customerList = connector.getCustomer();
            for (int i = 0; i<vehicleList.size(); i++)
            {
                row1[0] = customerList.get(i).getID();
                row1[1] = customerList.get(i).getName();
                row1[2] = customerList.get(i).getPhone();
                model1.addRow(row1);
                    
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
            
        
        
    }//GEN-LAST:event_rental_SearchbtnActionPerformed

