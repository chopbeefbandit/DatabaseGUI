    private void rental_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rental_enterActionPerformed
        // TODO add your handling code here:
        
        try {
            
            String startDate = rental_date1Label.getText();
            String returnDate = rental_date2Label.getText();
            
            LocalDate date1 = LocalDate.parse(startDate);
            LocalDate date2 = LocalDate.parse(returnDate);
            
            long daysBetween = ChronoUnit.DAYS.between(date1, date2);
            String days =daysBetween + "";
            
            String[] customerRow;
            customerRow = selectedCustomer[selectedRowC].toString().substring(1, selectedCustomer[selectedRowC].toString().length()-1).split("\\, ");
            String custID = customerRow[0];
            System.out.println(custID);
            String[] vehicleRow;
            vehicleRow = selectedVehicle[selectedRowV].toString().substring(1, selectedVehicle[selectedRowV].toString().length()-1).split("\\, ");
            String vin = vehicleRow[0];
            System.out.println(vin);
            String orderDate = orderDate_inputField.getText(); 
            String rentalType = rental_rentalType.getSelectedItem().toString();
            String qty = qty_combo.getSelectedItem().toString();
            String choice = payment_combo.getSelectedItem().toString();
            String paymentDate;
            String returned;
            if (choice == "Today")
            {
                paymentDate = rental_date1Label.getText();
                returned = "1";
            }
            else
            {
                paymentDate = rental_date2Label.getText();
                returned = "0";
            }
           
            connector.insertRental(custID, vin, startDate, orderDate, rentalType, qty, returnDate, paymentDate, returned, days);
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
        
        
    }//GEN-LAST:event_rental_enterActionPerformed

